package org.webcomponents.summary;


public class Cluster {

	private long count;
	private String country;
	private String area;
	private String region;
	private String province;
	private String genderLabel;
	private String ageRangeLabel;
	
	public final long getCount() {
		return count;
	}
	public final void setCount(long count) {
		this.count = count;
	}
	public final String getCountry() {
		return country;
	}
	public final void setCountry(String country) {
		this.country = country;
	}
	public final String getArea() {
		return area;
	}
	public final void setArea(String area) {
		this.area = area;
	}
	public final String getRegion() {
		return region;
	}
	public final void setRegion(String region) {
		this.region = region;
	}
	public final String getProvince() {
		return province;
	}
	public final void setProvince(String province) {
		this.province = province;
	}
	public final String getGenderLabel() {
		return genderLabel;
	}
	public final void setGenderLabel(String gender) {
		this.genderLabel = gender;
	}
	public final String getAgeRangeLabel() {
		return ageRangeLabel;
	}
	public final void setAgeRangeLabel(String ageRange) {
		this.ageRangeLabel = ageRange;
	}	
	
}
